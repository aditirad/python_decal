import numpy as np
import matplotlib.pyplot as plt

def make_sine_wave(x, A, w):
    """Return a sine wave with amplitude A and angular frequency w."""
    return A * np.sin(w * x)

# Create x from 0 to 2Ï€ with 1000 points
x = np.linspace(0, 2 * np.pi, 1000)

# Amplitudes and frequencies
amplitudes = [0.5, 1, 1.5, 2, 2.5]
frequencies = [1, 2, 3, 4, 5]

# Create a figure
plt.figure(figsize=(10, 6))

# Plot sine waves
line_styles = ['-', '--', '-.', ':', (0, (3, 5, 1, 5))]  # bonus: different line styles
for A, w, style in zip(amplitudes, frequencies, line_styles):
    y = make_sine_wave(x, A, w)
    plt.plot(x, y, linestyle=style, label=f"A={A}, w={w}")

# Add labels and legend
plt.title("Sine Waves with Varying Amplitudes and Frequencies")
plt.xlabel("x")
plt.ylabel("y")
plt.legend()
plt.grid(True)
plt.show()

